// 1. Hari senin - Jum'at

function angkaHari(hari) {
    switch (hari) {
        case 1:
            return "Senin"
            break;
        case 2 :
            return "Selasa"
            break;
        case 3 :
            return "Rabu"
            break;
        case 4 :
            return "Kamis"
            break;
        case 5 :
            return "Jum'at"
            break;
        default:
            return "Libur"
            break;
    }
}
